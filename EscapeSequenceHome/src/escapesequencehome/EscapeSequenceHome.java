
package escapesequencehome;

public class EscapeSequenceHome {

    
    public static void main(String[] args) {
       
        System.out.println("Java\tPython\tPHP");
        System.out.println("Java \bPython \bPHP");
        System.out.println("Java \nPython \nPHP");
        System.out.println("Java \'Python\' PHP");
        System.out.println("Java \"Python\" PHP");
        System.out.println("Java \\Python\\PHP");

    }
    
}
